"""creative_worker.py - Director Creativo: After Effects + Remotion + FFmpeg.

Estrategia hibrida:
  1. PRIMERA OPCION: codigo (Remotion + FFmpeg) - 100% determinista, sin GUI
  2. PREMIUM: After Effects ExtendScript - modifica plantillas .aep
     programaticamente, abre AE via CLI, dispara render desde script o GUI
  3. FALLBACK: GUI via SoM - solo si AE no acepta CLI

After Effects CLI:
  afterfx.exe -r "C:\path\to\script.jsx"
  afterfx.exe -m -r script.jsx  (modo headless si disponible)

Templates location: data/ae_templates/ (proyectos .aep + scripts .jsx asociados)
Output: data/ae_renders/<timestamp>/
"""
from __future__ import annotations

import json
